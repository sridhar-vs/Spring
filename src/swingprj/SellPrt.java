    private void AsusSel1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AsusSel1ActionPerformed
        try{
        String sql = "exec sp_fetchPd 'Asus'";
        productDetails pd = new productDetails();
        ResultSet res = pd.res(sql);
        int i=1;
        while(res.next()){
        if(i==1){
        DefaultTableModel model = (DefaultTableModel)Tableres.getModel();
        String name = res.getString("prt_Name");
        int price = res.getInt("price");
        int Qun=res.getInt("Quantity");
        int val1 = (int) Asusval1.getValue();
        price=price*val1;
        if(val1>0&&Qun>=val1){
        model.insertRow(Tableres.getRowCount(), new Object[] {name,val1, price});
        Qun = Qun-val1;
        String sql1 = "exec sp_upQun '"+Qun+"','Asus001'";
            productDetails.Update(sql1);
            Asuspdtname1.setText(res.getString("prt_Name"));
            if (Qun >= 1) {
                Asusprc1.setText("₹" + res.getString("price"));
            } else {
                Asusprc1.setText("Out Of Stock");
            }
        Asusval1.setValue(0);
        }
        }
        i++;
        }
        }
        catch(Exception ex){
            System.out.println(ex);
        }
    }//GEN-LAST:event_AsusSel1ActionPerformed

